#Упражнение 1: Операции со строкой
text = "Python Programming"
print(f"Длина строки: {len(text)}") # 1. Выводит длину строки
print(text[7]) # 2. Выводит символ по индексу 7
print(text[-3:]) # 3. Выводит последние 3 символа
print("gram" in text); # 4. Проверяет, содержится ли в строке подстрока "gram"
