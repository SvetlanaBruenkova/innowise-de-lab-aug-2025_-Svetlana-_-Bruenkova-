#Упражнение 3: Методы списка
fruits = ["apple", "banana"]
# 1. Добавьте "orange" в конец списка
fruits.append("orange")
print(f"Добавили: {fruits}")
# 2. Вставьте "grape" по индексу 1
fruits.insert(1,"grape")
print(f"Вставили: {fruits}")
# 3. Удалите "banana"
fruits.remove("banana")
print(f"Удалили: {fruits}")
# 4. Отсортируйте список
fruits.sort()
print(f"Отсортирвали: {fruits}")
# 5. Переверните список
fruits.reverse()
print(f"Перевернули: {fruits}")